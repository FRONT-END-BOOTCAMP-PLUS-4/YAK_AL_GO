export interface SimplifiedMedicine {
  itemSeq: string;
  shortName: string; // κ°„μ†ν™”λ μμ•½ν’λ…
  shortManufacturer: string; // κ°„μ†ν™”λ μ μ΅°μ‚¬λ…
  category: CategoryInfo; // μ¤λ§νΈ μΉ΄ν…κ³ λ¦¬ μ •λ³΄
  originalName: string; // μ›λ³Έ μμ•½ν’λ… (ν΄νμ©)
  originalManufacturer: string; // μ›λ³Έ μ μ΅°μ‚¬λ… (ν΄νμ©)
  originalCategory: string; // μ›λ³Έ μΉ΄ν…κ³ λ¦¬ (ν΄νμ©)
  displayName: string; // μ‹¤μ  ν‘μ‹λ  μμ•½ν’λ… (μ¤‘λ³µ μ‹ μ›λ³Έ μ‚¬μ©)
}

/**
 * μμ•½ν’ μ „μ²΄ μ •λ³΄ κ°„μ†ν™”
 * @param medicine μ›λ³Έ μμ•½ν’ μ •λ³΄
 * @returns κ°„μ†ν™”λ μμ•½ν’ μ •λ³΄
 */
export function formatMedicineInfo(medicine: {
  itemSeq: string;
  itemName: string;
  entpName?: string;
  classNo?: string;
}): SimplifiedMedicine {
  const category = mapClassNoToCategory(medicine.classNo || '');

  return {
    itemSeq: medicine.itemSeq,
    shortName: formatMedicineName(medicine.itemName),
    shortManufacturer: formatManufacturer(medicine.entpName || ''),
    category: category,
    originalName: medicine.itemName,
    originalManufacturer: medicine.entpName || '',
    originalCategory: medicine.classNo || '',
    displayName: formatMedicineName(medicine.itemName),
  };
}

/**
 * μ¤‘λ³µ μμ•½ν’λ… μ²λ¦¬ - μ¤‘λ³µ μ‹ μ›λ³Έ μ΄λ¦„ μ‚¬μ©
 * @param medicines μμ•½ν’ λ©λ΅
 * @returns μ¤‘λ³µ μ²λ¦¬λ μμ•½ν’ λ©λ΅
 */
export function processDisplayNames(
  medicines: {
    itemSeq: string;
    itemName: string;
    entpName?: string;
    classNo?: string;
  }[]
): SimplifiedMedicine[] {
  // 1. κΈ°λ³Έ ν¬λ§·ν… μ μ©
  const formattedMedicines = medicines.map((medicine) => formatMedicineInfo(medicine));

  // 2. κ°„μ†ν™”λ μ΄λ¦„λ³„λ΅ κ·Έλ£Ήν™”
  const nameGroups = formattedMedicines.reduce(
    (groups, medicine) => {
      const name = medicine.shortName;
      if (!groups[name]) {
        groups[name] = [];
      }
      groups[name].push(medicine);
      return groups;
    },
    {} as Record<string, SimplifiedMedicine[]>
  );

  // 3. μ¤‘λ³µλ μ΄λ¦„λ“¤μ€ μ›λ³Έ μ΄λ¦„ μ‚¬μ©
  for (const [, group] of Object.entries(nameGroups)) {
    if (group.length > 1) {
      // μ¤‘λ³µλ κ²½μ° μ›λ³Έ μ΄λ¦„ μ‚¬μ©
      for (const medicine of group) {
        medicine.displayName = medicine.originalName;
      }
    }
  }

  return formattedMedicines;
}

