/**
 * CLASS_NOλ¥Ό κΈ°λ°μΌλ΅ μ¤λ§νΈ μΉ΄ν…κ³ λ¦¬ λ§¤ν•‘
 * @param classNo μ›λ³Έ λ¶„λ¥λ²νΈ "[214]νμ••κ°•ν•μ "
 * @returns μ‚¬μ©μ μΉν™”μ  μΉ΄ν…κ³ λ¦¬ μ •λ³΄
 */
export function mapClassNoToCategory(classNo: string): CategoryInfo {
  if (!classNo) return getDefaultCategory();

  // μ°μ„ μμ„λ³„λ΅ μ •λ ¬λ κ·μΉ™μΌλ΅ λ§¤μΉ­
  const sortedRules = CATEGORY_RULES.sort((a, b) => a.priority - b.priority);

  for (const rule of sortedRules) {
    if (rule.keywords.some((keyword) => classNo.includes(keyword))) {
      return {
        id: rule.id,
        display: rule.display,
        icon: rule.icon,
        color: rule.color,
        original: classNo,
      };
    }
  }

  return {
    ...getDefaultCategory(),
    original: classNo,
  };
}

