/**
 * μμ•½ν’λ… κ°„μ†ν™”
 * @param itemName μ›λ³Έ μμ•½ν’λ… "λ‹νλ””μ¨CRμ„λ°©μ •40λ°€λ¦¬κ·Έλ(λ‹νλ””ν•€)"
 * @returns κ°„μ†ν™”λ μμ•½ν’λ… "λ‹νλ””μ¨CR"
 */
export function formatMedicineName(itemName: string): string {
  if (!itemName) return '';

  let formatted = itemName;

  // 1. κ΄„νΈ μ• μ„±λ¶„λ… μ κ±° (λ§μ§€λ§‰ κ΄„νΈλ§)
  formatted = formatted.replace(/\([^)]*\)$/, '');

  // 2. "/" λ¬Έμ μ κ±° (μ¬λμ‹λ΅ κµ¬λ¶„λ μ΄λ¦„λ“¤ μ •λ¦¬)
  formatted = formatted.replace(/\/.*$/, ''); // μ²« λ²μ§Έ "/" μ΄ν›„ λ¨λ“  λ‚΄μ© μ κ±°
  formatted = formatted.replace(/\//g, ''); // λ‚¨μ€ "/" λ¬Έμλ“¤ μ κ±°

  // 3. μ©λ‰ μ •λ³΄ κ°„μ†ν™”
  formatted = formatted.replace(/(\d+)(λ°€λ¦¬κ·Έλ|λ°€λ¦¬κ·Έλ¨|mg|κ·Έλ|g)/gi, '');

  // 4. λ¶ν•„μ”ν• μ ν• λ‹¨μ–΄ μ κ±°
  const unnecessaryWords = ['μ„λ°©μ •', 'CRμ •', 'ERμ •', 'ν•„λ¦„μ½”ν…μ •', 'μ¥μ©μ •', 'μΊ΅μν•', 'μ •μ '];
  for (const word of unnecessaryWords) {
    formatted = formatted.replace(new RegExp(word, 'gi'), '');
  }

  // 5. μµλ€ κΈΈμ΄ μ ν• (15μ)
  if (formatted.length > 15) {
    formatted = `${formatted.substring(0, 12)}...`;
  }

  // 6. κ³µλ°± μ •λ¦¬
  return formatted.trim().replace(/\s+/g, ' ');
}

